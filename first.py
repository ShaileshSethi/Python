# Python code to demonstrate string formatting using f-strings

#string
name = "bro"
food = "pizza"

# Integers
age = 19
quantity = 5  


print("String formatting using f-strings:\n")
print(f"hello {name} ")
print(f"food {food}\n ")


print("Integer formatting using f-strings:\n")
print(f"My age is {age} ")
print(f"quantity is {quantity} ")